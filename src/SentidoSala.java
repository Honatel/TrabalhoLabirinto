
public class SentidoSala {
	public int Norte;
	public int Sul;
	public int Leste;
	public int Oeste;
	public int Up;
	public int Down;
	public String nome;
	
	public int getNorte() {
		return Norte;
	}
	public void setNorte(int norte) {
		Norte = norte;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getSul() {
		return Sul;
	}
	public void setSul(int sul) {
		Sul = sul;
	}
	public int getLeste() {
		return Leste;
	}
	public void setLeste(int leste) {
		Leste = leste;
	}
	public int getOeste() {
		return Oeste;
	}
	public void setOeste(int oeste) {
		Oeste = oeste;
	}
	public int getUp() {
		return Up;
	}
	public void setUp(int up) {
		Up = up;
	}
	public int getDown() {
		return Down;
	}
	public void setDown(int down) {
		Down = down;
	}
}
